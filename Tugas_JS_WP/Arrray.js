//Array:

// Bro, Array itu kayak rak buku. Lo bisa simpen banyak buku dalam satu rak, dan tiap buku punya tempatnya sendiri.

// Di JavaScript, Array itu bisa simpen banyak data, dan tiap data punya 'index' sebagai nomor urutnya.

// Contoh Array sederhana:
let temanTeman = ["Andi", "Budi", "Cici", "Dodi"];
console.log("Daftar Teman: ", temanTeman);

// Studi Kasus: Misalnya lo mau tahu siapa teman yang paling sering lo ajak main.
// Lo bisa akses teman lo berdasarkan indexnya. Ingat, index di JavaScript dimulai dari 0.
console.log("Teman main sering: ", temanTeman[1]); // Output: Budi

// Lo juga bisa update data di Array. Misalnya, lo punya teman baru namanya 'Eka'.
temanTeman[4] = "Eka";
console.log("Update Daftar Teman: ", temanTeman);

// Nah, gampang kan? Array itu membantu lo untuk simpen banyak data dalam satu tempat yang terorganisir.




