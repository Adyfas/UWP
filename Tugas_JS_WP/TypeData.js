//TypeData:
// Bro, di JS itu ada beberapa tipe data yang harus lo kenal biar bisa ngoding dengan lancar. Yuk, kita bahas satu-satu.

// 1. String: Tipe data ini buat nyimpen teks, bro. Apa aja yang diapit tanda kutip, itu string. Contoh: let nama = "Budi";
console.log("Contoh String: ", "Halo, bro!");

// 2. Number: Ini tipe data buat angka, gak peduli itu integer atau floating point. Contoh: let umur = 21;
console.log("Contoh Number: ", 2021);

// 3. Boolean: Tipe data ini cuma ada dua nilai, true atau false. Cocok buat kondisi yang iya atau tidak. Contoh: let sudahMakan = true;
console.log("Contoh Boolean: ", true);

// 4. Null: Ini tipe data yang artinya 'tidak ada nilai'. Biasanya dipake kalo misalnya lo mau reset nilai variabel. Contoh: let rahasia = null;
console.log("Contoh Null: ", null);

// 5. Undefined: Tipe data ini muncul kalo variabel udah dideklarasikan tapi belum diberi nilai. Contoh: let bingung;
console.log("Contoh Undefined: ", undefined);

// 6. Object: Bro, ini tipe data yang bisa nyimpen banyak nilai dalam satu variabel. Bisa berupa pasangan key dan value. Contoh: let mobil = {merk: 'Toyota', tahun: 2020};
console.log("Contoh Object: ", {jenis: "Motor", merk: "Honda"});

// 7. Array: Mirip object, tapi ini buat nyimpen list nilai yang biasanya tipe data sama. Contoh: let angka = [1, 2, 3, 4, 5];
console.log("Contoh Array: ", [1, 2, 3, 4, 5]);

// 8. Symbol: Tipe data baru di ES6, unik karena setiap symbol dijamin unik. Cocok buat key di object. Contoh: let simbol = Symbol('id');
console.log("Contoh Symbol: ", Symbol('id'));

// Nah, itu dia tipe-tipe data yang ada di JavaScript. Masing-masing punya kegunaan dan situasi khusus buat dipake. Jadi, pahami dan gunakan dengan bijak, bro!